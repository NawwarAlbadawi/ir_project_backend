    "    def get_doc_topic(self, doc_id): return self._map.get(doc_id)",
    "    def get_all_topics(self): return self._topics",
    "    def infer_topic(self, text):",
    "        v = self._vec.transform([text])",
    "        d = self._lda.transform(v)[0]",
