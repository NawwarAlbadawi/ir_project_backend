    "            words = [fn[j] for j in idx]",
    "            self._topics.append({\"topic_id\": i, \"label\": \"/\".join(words[:3]),",
    "                \"top_words\": words, \"doc_count\": counts[i]})",
    "        self._map = {}",
    "        doc_ids = [d[\"doc_id\"] for d in documents]",
    "        for i, did in enumerate(doc_ids):",
    "            t = self._topics[tids[i]]",
