    "    return {\"dataset\": dataset_name, \"num_topics\": s.model.num_topics, \"topics\": s.model.get_all_topics()}",
    "@app.post(\"/topics/detect\", response_model=DocTopicResponse)",
    "def detect(req: DocTopicRequest):",
    "    s = _get(req.dataset_name)",
