    "    DocTopicRequest, DocTopicResponse, DocTopicResult, TopicStatusResponse)",
    "logger = logging.getLogger(\"topic_service\")",
    "logging.basicConfig(level=logging.INFO, format=\"%(asctime)s|%(levelname)s|%(message)s\")",
    "PREP = \"http://localhost:8001\"",
    "DATA = Path(__file__).resolve().parent.parent.parent / \"data\"",
    "class State:",
    "    def __init__(self, n):",
    "        self.name = n; self.status = \"not_built\"; self.error = None",
    "        self.model = LDAModel(); self._lock = threading.Lock()",
    "_states = {}; _lock = threading.Lock()",
    "def _get(n):",
    "    with _lock:",
    "        if n not in _states: _states[n] = State(n)",
    "        return _states[n]",
    "@asynccontextmanager",
    "async def lifespan(app):",
    "    if DATA.exists():",
    "        for d in DATA.iterdir():",
    "            if d.is_dir():",
    "                s = _get(d.name)",
    "                if s.model.load(d / \"lda_model.pkl\"):",
    "                    with s._lock: s.status = \"ready\"",
    "    logger.info(\"Topic Service ready.\")",
    "    yield",
    "app = FastAPI(title=\"Topic Detection Service\", lifespan=lifespan)",
    "app.add_middleware(CORSMiddleware, allow_origins=[\"*\"], allow_methods=[\"*\"], allow_headers=[\"*\"])",
    "@app.get(\"/health\")",
    "def health(): return {\"status\": \"ok\", \"service\": \"topic_detection\"}",
    "@app.post(\"/topics/build\", response_model=BuildTopicsResponse)",
