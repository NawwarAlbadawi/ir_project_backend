    "logger = logging.getLogger(\"topic_service.lda\")",
    "def _tok(t): return t.split()",
    "class LDAModel:",
    "    def __init__(self):",
    "        self._lda = self._vec = None",
    "        self._topics = []",
    "        self._map = {}",
    "        self._n = 0",
    "    def build(self, documents, num_topics=10, num_top_words=8):",
    "        self._n = num_topics",
    "        corpus = [d.get(\"cleaned\", \" \".join(d.get(\"tokens\", []))) for d in documents]",
    "        self._vec = CountVectorizer(tokenizer=_tok, preprocessor=None, lowercase=False,",
    "            token_pattern=None, max_df=0.95, min_df=5, max_features=20_000)",
    "        dtm = self._vec.fit_transform(corpus)",
    "        self._lda = LatentDirichletAllocation(n_components=num_topics, max_iter=15,",
    "            learning_method=\"online\", batch_size=512, random_state=42, n_jobs=-1)",
