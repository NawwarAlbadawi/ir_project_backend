    "    for did in req.doc_ids:",
    "        info = s.model.get_doc_topic(did)",
    "        if info:",
    "            results.append(DocTopicResult(doc_id=did, topic_id=info[\"topic_id\"],",
    "                topic_label=info[\"topic_label\"], top_words=info[\"top_words\"],",
    "                probability=info[\"probability\"]))",
    "        else:",
    "            results.append(DocTopicResult(doc_id=did, topic_id=-1,",
    "                topic_label=\"unknown\", top_words=[], probability=0.0))",
    "    return DocTopicResponse(dataset=req.dataset_name, results=results)",
    "@app.get(\"/topics/infer\")",
    "def infer(dataset_name: str = Query(...), text: str = Query(...)):",
    "    s = _get(dataset_name)",
    "    if s.status != \"ready\": raise HTTPException(404, \"Not ready.\")",
    "    return {\"dataset\": dataset_name, \"topic\": s.model.infer_topic(text)}",
    "def _pipeline(state, req):",
    "    with state._lock: state.status = \"building\"; state.error = None",
    "    try:",
