    "        d = DATA / req.dataset_name; d.mkdir(parents=True, exist_ok=True)",
    "        state.model.save(d / \"lda_model.pkl\")",
    "        with state._lock: state.status = \"ready\"",
    "        logger.info(f\"{req.dataset_name} LDA ready.\")",
    "    except Exception as e:",
    "        logger.exception(e)",
    "        with state._lock: state.status = \"error\"; state.error = str(e)",
    "def _fetch(name):",
    "    docs = []; off = 0",
    "    with httpx.Client(timeout=120) as c:",
    "        while True:",
    "            r = c.get(f\"{PREP}/dataset/docs\",",
    "                params={\"dataset_name\": name, \"offset\": off, \"limit\": 5000})",
    "            r.raise_for_status()",
    "            batch = r.json().get(\"documents\", [])",
    "            if not batch: break",
    "            docs.extend(batch); off += len(batch)",
    "            logger.info(f\"Fetched {len(docs)} docs...\")",
    "    return docs",
    "'''",
